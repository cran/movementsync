#' NIR_ABh_Puriya_Central_Pose_Tabla
#'
#' A subset of data from NIR_ABh_Puriya the estimate
#' pose of the tabla player.
#' The data comes from a collection of audiovisual recordings of 
#' North Indian (Hindustani) raga performances which are
#' part of IEMP North Indian Raga collection, collected and
#' curated by Martin Clayton, Laura Leante, and Simone Tarsitani.
#'
#' @format ## `rda`
#' A data frame with 1,501 rows and 27 columns:
#' \describe{
#'   \item{X}{frame number, here 25 fps}
#'   \item{LEar_x}{X coordinate of Left Ear}
#'   \item{LEar_y}{Y coordinate of Left Ear}
#'   \item{LElbow_x}{X coordinate of Left Elbow}
#'   \item{LElbow_y}{Y coordinate of Left Elbow}
#'   \item{LEye_x}{X coordinate of Left Eye}
#'   \item{LEye_y}{Y coordinate of Left Eye}
#'   \item{LShoulder_x}{X coordinate of Left Shoulder}
#'   \item{LShoulder_y}{Y coordinate of Left Shoulder}
#'   \item{LWrist_x}{X coordinate of Left Wrist}
#'   \item{LWrist_y}{Y coordinate of Left Wrist}
#'   \item{MidHip_x}{X coordinate of Left MidHip}
#'   \item{MidHip_y}{Y coordinate of Left MidHip}
#'   \item{Neck_x}{X coordinate of Left Neck}
#'   \item{Neck_y}{Y coordinate of Left Neck}
#'   \item{Nose_x}{X coordinate of Left Nose}
#'   \item{Nose_y}{Y coordinate of Left Nose}
#'   \item{REar_x}{X coordinate of Right Ear}
#'   \item{REar_y}{Y coordinate of Right Ear}
#'   \item{RElbow_x}{X coordinate of Right Elbow}
#'   \item{RElbow_y}{Y coordinate of Right Elbow}
#'   \item{REye_x}{X coordinate of Right Eye}
#'   \item{REye_y}{Y coordinate of Right Eye}
#'   \item{RShoulder_x}{X coordinate of Right Shoulder}
#'   \item{RShoulder_y}{Y coordinate of Right Shoulder}
#'   \item{RWrist_x}{X coordinate of Right Wrist}
#'   \item{RWrist_y}{Y coordinate of Right Wrist}
#'   ...
#' }
#' @usage data(NIR_ABh_Puriya_Central_Pose_Tabla)
#' @source \url{https://osf.io/tj2n5}
"NIR_ABh_Puriya_Central_Pose_Tabla"
